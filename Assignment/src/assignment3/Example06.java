package assignment3;

public class Example06 {

	public static void main(String[] args) {
		int i = 100; // 정수형 i 선언
		long l = i;  // 더 큰 정수형 l 할당 -> long
		float f = l; // 더 큰 실수형에 f 할당 -> float
		System.out.println(i); // 정수
		System.out.println(l); // long
		System.out.println(f); //; float
	}
}
