package assignment3;

public class TypeConversion01 {

	public static void main(String[] args) {
		int x = 10;     // 정수형 x 선언
		double y = 3.0; // 실수형 y 선언
		// 실수형이 더 크므로 자동 형 변환 -> 실수로 출력
		System.out.println(x + y);
		System.out.println(x - y);
		System.out.println(x * y);
		System.out.println(x / y);
	}
}
