package assignment3;

public class Example07 {

	public static void main(String[] args) {
		// 더 작은 변수를 할당하므로 캐스팅 형
		double d = 3.14;  // 실수형 변수 d 선언
		long l = (long)d; // 정수형 변수 long l 선언
		int i = (int) l;  // 정수형 변수 ㅑ 선언
		System.out.println(d); // double형태
		System.out.println(l); // long형태
		System.out.println(i); // int형태
	}

}
