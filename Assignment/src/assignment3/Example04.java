package assignment3;

public class Example04 {

	public static void main(String[] args) {
		int x = 10; // 정수형 변수 x 선언
		System.out.println(x); // x 값인 10출력
		x += 2; // 기존 10에 2 더한 12 대입
		System.out.println(x); // 12출력
		x *= 2; // 기존 12에 2 곱한 24 대입
		System.out.println(x); // 24출력
		x -= 2; // 기존 24에 2 뺀 22 대입
		System.out.println(x); // 22출력
	}

}
